				<span class="mainTitle"><b>Concours de poésie</b> <br/>Château Mercier</span>
				<br/><br/>
				Le concours de poésie comprend deux catégories : les livres de poésie et les poèmes, en français
				et en allemand. <br/><br/>
				M. Jacques Tornay, Mme Catherine Seylaz-Dubuis et
				M. Curdin Ebneter, membres du jury du concours de poésie du Festival
				Rilke, ont analysé les 38 livres de poésie en français reçus dans la
				catégorie A, alors que M. Bernard Böschenstein, M. Rätus Luck et
				M. Engelbert Reul se sont penchés sur les 45 ouvrages en allemand.<br/><br/>
				Pour la catégorie B, le thème imposé était «FRONTIERES / GRENZEN»,
				thème décliné par 17 participants de langue française, dont les textes
				ont été analysés par Mme Jocelyne Gagliardi, Mme Letizia Scattolin
				Hagin et M. Serge Rey. <br/><br/>Les 50 poèmes de langue allemande ont été
				examinés par M. Hubert Theler, M. Michel Mettler et M. Martin Zingg.

